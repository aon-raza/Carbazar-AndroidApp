package com.example.carbazar.Models;

public class chatOptions {
    private String label;
    private String text;

    public chatOptions() {
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
